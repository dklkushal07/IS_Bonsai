// toggle1 function to blur the background and bring out the popup component in the DOM
function toggle1(){
    var blur = document.getElementById('blur');     //brings element with id(blur) and implements the active css in it
    blur.classList.toggle('active');
    var popup1 = document.getElementById('popup1');     //brings element with id(popup1) and implements the active css in it
    popup1.classList.toggle('active');
}
